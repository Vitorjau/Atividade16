public class Veiculos {
    String marca;
    String modelo;
    String cor;
    int ano;
    int rodas;

    public Veiculos(String marca, String modelo, String cor, int ano, int rodas) {
        this.marca = marca;
        this.modelo = modelo;
        this.cor = cor;
        this.ano = ano;
        this.rodas = rodas;
    }
}